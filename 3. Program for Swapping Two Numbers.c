////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////// Satement: Program For Swapping Two Numbers.
//////////////       Input : 11 10
///////////////////  Output : 10 11
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#include<stdio.h>
#include<conio.h>
int main()
{
    int No1 = 0, No2 = 0,temp = 0;
    printf("\n #######****_____ Swapping For Two Number _____****#######\n");

    printf("\n======================================================\n");

    printf("\n Enter Two Number : ");
    scanf("%d%d",&No1,&No2);

    printf("\n======================================================\n");

    printf("\n Values of No1 & No2 before swapping No1=%d No2=%d",No1,No2);

    temp = No1;
    No1 = No2;
    No2 = temp;

    printf("\n======================================================\n");

    printf("\n Values of No1 & No2 After swapping No1=%d No2=%d",No1,No2);

    printf("\n======================================================\n");

    printf("\n \n Thank You......!!!!!");
    printf("\n======================================================\n");

    getch();
    return 0;
}
