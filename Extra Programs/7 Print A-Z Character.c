#include<stdio.h>
#include<conio.h>
int main()
{
    char ch;

    printf("\n =========== **** Print A-Z Pattern **** ================\n");

    printf("\n---------------------------------------------------------------------------------\n");

    for(ch='A';ch<='Z';ch++)
    {
        printf("%c",ch);
        printf("\t");
    }

    printf("\n---------------------------------------------------------------------------------\n");

    printf("\n Thank You");

    printf("\n-----------------------------------------------------------------------------------\n");

    getch();
    return 0;
}
