#include<stdio.h>
#include<conio.h>
int main()
{

    int   No=0,i=0,j=0;  /// Hint: -- Row=i;col=j;

    printf("\n -----------------------------------------  $$$$  O - Pattern.c  $$$$ ------------------------------------------------- \n");

    printf("\n=======================================================================================\n");

    printf("\n Enter a number to print pattern : ");
    scanf("%d",&No);

    printf("\n=======================================================================================\n");

    for(i=1;i<=No;i++)
    {
        for(j=1;j<=No;j++)
        {
                    if((i==1)||(j==1)||(i==No)||(j==No))
                    {
                        printf(" * ");
                    }
                    else
                    {
                        printf("   ");
                    }
        }
        printf("\n");
    }

    printf("\n=======================================================================================\n");

    printf("\n \n Thank You");

    printf("\n=======================================================================================\n");

    getch();
    return 0;

}
