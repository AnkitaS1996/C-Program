#include<stdio.h>
#include<conio.h>
int main()
{

    int   No=0,row=0,col=0;

    printf("\n ----------------------------------------------------  $$$$  Simple * Pattern  $$$$----------------------------------------------------------------- \n");

    printf("\n=======================================================================================\n");

    printf("\n Enter a number to print pattern : ");
    scanf("%d",&No);

    printf("\n=======================================================================================\n");

    for(row=1;row<=No;row++)
    {

        for(col=1;col<=No;col++)

        {
            printf(" * ");
        }

        printf("\n");

    }

    printf("\n=======================================================================================\n");

    printf("\n \n Thank You");

    printf("\n=======================================================================================\n");

    getch();
    return 0;


}
