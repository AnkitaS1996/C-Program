#include<stdio.h>
#include<conio.h>
int main()
{
    int No = 0,i = 0, j = 0;
    int Num = 11;

    printf("\n##########**** Upper Left Triangular Number Pattern .c****##########\n");

    printf("\n==================================================\n");
    printf("\n Enter Number to print pattern : ");
    scanf("%d",&No);
    printf("\n==================================================\n");

    for(i = 1;i <= No;i++)
    {
        for(j = 1;j <= No;j++)
        {
            if(i+j <= No+1)
            {
                printf(" %d ",Num);
            }
            Num++;
        }
        printf("\n");
    }

    printf("\n==================================================\n");
    printf("\n Thank You..!!!!");
    printf("\n==================================================\n");

    getch();
    return 0;
}
