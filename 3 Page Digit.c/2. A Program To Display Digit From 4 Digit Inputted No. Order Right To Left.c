#include<stdio.h>
#include<conio.h>
int main()
{
            int No = 0, digit = 0;

            printf("\n======================================================\n");
            printf("\n Enter Number To Check Whether it's 4 Digit Or Not.. :  ");
            scanf("%d",&No);
            printf("\n======================================================\n");

            while(No > 0)
            {
                No = No/10;
                digit++;
            }

            if(digit == 4)
            {
                        printf("\n Given Number is 4 digit Number...!!!");
                        printf("\n======================================================\n");
            }
            else
            {
                        printf("\n Given Number Is Not 4 digit Number..!!!");
                        printf("\n======================================================\n");
            }

            printf("\n======================================================\n");
            printf("\n  Thank You....!!!");
            printf("\n======================================================\n");

}
