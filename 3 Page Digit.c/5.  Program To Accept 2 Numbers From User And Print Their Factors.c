#include<stdio.h>
#include<conio.h>
int main()
{
    int No = 0, temp = 0,i = 1;

    printf("\n===============**** Factors Of Given Number ****================\n");

    printf("\n===============******************************=======================\n");
    printf("\n Enter Number to Print their Factors : ");
    scanf("%d",&No);
    printf("\n===============******************************=======================\n");

    temp = No;

    if(temp <= 0)
    {
        printf("\n Invalid Numbers...!!!!");
    }

   while(temp >= i)
   {
       if(temp % i == 0)
       {
           printf("\n %d",i);
       }
       i++;
   }

    printf("\n===============******************************=======================\n");
    printf("\n Thank you.....!!!! ");
    printf("\n===============******************************=======================\n");

    getch();
    return 0;
}
