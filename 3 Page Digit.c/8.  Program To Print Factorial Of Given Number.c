/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////    Statement =  Program To Print Factorial Of Given Number.
////            Input = 5
////         Output = 120  (5*4*3*2*1)
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#include<stdio.h>
#include<conio.h>
int main()
{
            int No = 0, Fact = 1, temp = 0;

            printf("\n********************************* Factorial Number ******************************************\n");

            printf("\n======================================================\n");
            printf("\n Enter Number To Print Factorial Of Given Number : ");
            scanf("%d",&No);
            printf("\n======================================================\n");

            temp = No;

            if(temp <= 0)
            {
                        printf("\n Invalid Number...!!!!");
                        printf("\n======================================================\n");
                        goto Down;
            }

            while(temp > 1)
            {
                        Fact = Fact * temp;
                        temp--;
            }

            printf("\n======================================================\n");
            printf("\n Given Number %d & Factorial  is = %d.",No,Fact);
            printf("\n======================================================\n");

            Down:
                printf("\n======================================================\n");
                printf("\n Thank You...!!!");
                printf("\n======================================================\n");

            getch();
            return 0;
}
