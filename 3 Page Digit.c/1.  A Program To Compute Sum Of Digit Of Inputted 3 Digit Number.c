#include<stdio.h>
#include<conio.h>
int main()
{
        int No = 0, temp = 0, Sum = 0,Digit = 0;

        printf("\n##########**** 3 Digit Number To Calculate Sum Using goto ****##########\n");

        printf("\n=======================================================\n");
        printf("\n Enter 3 Digit Number to Compute Sum Of Digit  : ");
        scanf("%d",&No);
        printf("\n=======================================================\n");

        temp = No;
        while(temp > 0)
        {
                temp = temp/10;
                Digit++;
        }
        if(Digit > 3  || Digit < 3)
        {
                printf("\n Invalid Number...!!!");
                printf("\n=======================================================\n");
                goto Down;
        }

        temp = No;
        while(temp > 0)
        {
                Sum = Sum + (temp%10);
                temp = temp / 10;  //// temp/=10;
        }
        printf("\n=======================================================\n");
        printf("\n Sum Of 3 Digit Number %d is = %d.",No,Sum);
        printf("\n=======================================================\n");

        printf("\n=======================================================\n");
        Down:
        printf("\n \n Thank You...!!!!");
        printf("\n=======================================================\n");

        getch();
        return 0;
}
